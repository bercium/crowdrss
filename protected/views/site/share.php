        <h2>Why we rate projects?</h2>
        <p>
          We believe that everyone should be able to see interesting projects. Even those buried beneath shear numbers 
          <br />
          <br />
          Currently there are around 800 new projects per day in the platforms on our list and we think that is just too overwhelming.
          <br />
          Every time you browse projects you do a split second decision if you want to check the project. And you do that by looking at the
          thumbnail, reading the title and maybe sometimes reading the short description. If project is doing really well you might see that from the
          funds they have already raised. 
          <br />
          And this is where we come in. We analyze the whole project presentation page and give you better suggestions based on that rather than just the first impression.

        </p>

        <hr>

        <h2>How we rate projects?</h2>
        <p>
          We started by analyzing over 160.000 projects and tried to determine what makes good projects good. Then we set a couple of different rating mechanisms to try and wage
          if project is good or not. First thing we do is rate the content then we check how much the project is being shared around social networks and we also make progressive checks
          to see how project is doing over time.
          <br /><br />We are still trying to find new ways of rating projects and backing that with the data from the past projects.
        </p>

        <h3>Rating content</h3>
        <p>
          By analyzing content we try to determine how much work author(s) have put into presenting their project.
          <br /><br />
          We check content length, number of images, extra videos, answered FAQ in general everything that adds some extra information to the viewer.
          <br />
          When content has a short description, has little or no images, no answered questions, no extra information or links pointing to other sources we assign it lower its scores.
          If someone didn't took the time to properly prepare the content than the question arises how prepared is he for the whole campaign.
          <br />
          Of course some small personal projects might get rated lower because of this.
        </p>

        <h3>Rating social reach</h3>
        <p>
          After project has been active for few hours we check how it  is doing on social networks.
          <br />
          One of the most important process of the whole campaign is spreading the word trough online media. That is why we believe this part is essential to a success of a project
          but not necessary to its quality.
        </p>

        <h3>Rating project progress</h3>
        <p>
          We check how much funds the project has gathered sofar. In combination with the elapsed time we predict the probability of project finishing successfully.
          This is just a rough approximation since we don't have enough time to see any emerging trends.
          <br />
          This is the smallest weight in the overall rating.
        </p>


        <hr>
        <h2>How often do we update ratings?</h2>
        <p>
          We rate project immediately after it's launch. 
          The first rating consists only from content analysis. Later in the day after the first couple of hours we also do social check to see how fast the project is spreading.
          Then after each day we take a snapshot the progress project made and recalculate their rating based on all parameters.
          <br /><br />
          After a week we consider project obsolete and we stop rating it.
        </p>

        <hr>
        <h2>How high should you set the threshold?</h2>
        <p>
          Here are some guidelines to help you decide:
          <br />
          <ul>
            <li>If you don't want to miss out on anything drag it all the way to the left and there won't be any rating limit.</li>
            <li>Preview what kind of projects different rating levels give you and then decide how high to set it.</li>
            <li>Or start with a lower rating and in time raise the bar if you get overwhelmed with the number of projects each day.</li>
          </ul>
          <br />
          We strive to push as many interesting and successful projects in the upper half of the rating, 
          but this is a learning process and we will improve it over time.
        </p>
